public class Esito {
    private String voto;
    private Boolean conferma;

    Esito(){
    }
    public void setVoto(String voto)
    {
        this.voto = voto;
    }
    public void setConferma(Boolean conferma)
    {
        this.conferma = conferma;
    }
    public String getVoto()
    {
        return voto;
    }
    public Boolean getConferma()
    {
        return conferma;
    }

}
